#include <libft.h>

void	ft_strrev(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = strlen(str) - 1;
	while (i < j)
	{
		c = str[i];
		str[i] = str[j];
		str[j] = c;
		i++;
		j--;
	}
}
